//Estos son Variables 

//Let se utiliza para declarar una variable con scope limitado
let pan = "pan de yuca";
pan = "pan de queso";

//Var se utiliza para declarar una variable sin scope limitado
var jugo= "jugo de naranja";
jugo = "jugo de limon";

//Constante se utiliza para declarar una variable que no cambia su valor
const pi = 3.1416;

console.log(pan);

console.log(jugo);

console.log(pi);
